// You spend 1.5 hours on social media daily. Check if your total weekly usage exceeds 10 hours.


let dailyUsage = 1.5;
let weeklyUsage = dailyUsage * 7;

if(weeklyUsage > 10){
    console.log("Exceeds 10 hours");
}else{
    console.log("Within limit");
}

console.log("Total weekly usage:", weeklyUsage);